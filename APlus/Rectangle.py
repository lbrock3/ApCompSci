# draw square in Python Turtle
import turtle
  
t = turtle.Turtle()
 
t.forward(100) # Forward turtle by 100 units
t.left(90) # Turn turtle by 90 degree
 
# drawing second side
t.forward(100) # Forward turtle by 100 units
t.left(90) # Turn turtle by 90 degree
 
# drawing third side
t.forward(100) # Forward turtle by s units
t.left(90) # Turn turtle by 90 degree
 
# drawing fourth side
t.forward(100) # Forward turtle by s units
t.left(90) # Turn turtle by 90 degree